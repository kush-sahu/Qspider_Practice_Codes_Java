class Switch 
{
	public static void main(String[] args) 
	{
		int a=1;

		switch(a){
			case 1:
			{
				System.out.println("Case 1 executed");
				break;
			}
			case 2:
			{
				System.out.println("Case 2 executed");
				break;
			}
			default :
			{
				System.out.println("Default executed");
				break;
			}
		}
	}
}
